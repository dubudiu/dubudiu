package com.dubudiu.test.displacement;

public class DisplacementTest {

    public static void main(String[] args) {
        int a,b,c;
        a = 10;
        b = 10;
        c = 10;
        a = a >> 2;
        b = b << 2;
        c = c >>> 1;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
